#!/usr/bin/env node
import { build } from 'vite'
import { copyFileSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const desktopDir = resolve(scriptDir, '..')
const entry = resolve(desktopDir, 'src/mcp/gmail/server.ts')
const output = resolve(scriptDir, 'gmail-mcp-server.mjs')
const tmp = mkdtempSync(join(tmpdir(), 'zspark-gmail-mcp-'))
const tmpOutput = join(tmp, 'gmail-mcp-server.mjs')

try {
  await build({
    configFile: false,
    publicDir: false,
    build: {
      emptyOutDir: true,
      minify: false,
      outDir: tmp,
      ssr: entry,
      target: 'node20',
      rollupOptions: {
        output: {
          entryFileNames: 'gmail-mcp-server.mjs',
          format: 'es'
        }
      }
    }
  })
  const bundled = readFileSync(tmpOutput, 'utf8').replace(/^#![^\n]*\n/, '')
  writeFileSync(
    tmpOutput,
    `#!/usr/bin/env node\n// Generated by scripts/build-gmail-mcp.mjs from src/mcp/gmail/server.ts.\n${bundled}`
  )
  copyFileSync(tmpOutput, output)
} finally {
  rmSync(tmp, { recursive: true, force: true })
}
